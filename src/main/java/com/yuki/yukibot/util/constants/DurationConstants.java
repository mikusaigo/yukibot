package com.yuki.yukibot.util.constants;

public class DurationConstants {

    // 1秒
    public static final int ONE_SECOND = 1;

    // 1分钟
    public static final int ONE_MINUTE = ONE_SECOND * 60;

    // 1小时
    public static final int ONE_HOUR = ONE_MINUTE * 60;

    // 半天
    public static final int HALF_DAY = ONE_HOUR * 12;

    // 一天
    public static final int ONE_DAY = HALF_DAY * 2;

    // 一周
    public static final int ONE_WEEK = ONE_DAY * 7;

    // 半月
    public static final int HALF_MONTH = ONE_DAY * 15;

    // 一月
    public static final int ONE_MONTH = HALF_MONTH * 2;

    // 半年
    public static final int HALF_YEAR = ONE_MONTH * 6;

    // 一年
    public static final int ONE_YEAR = ONE_DAY * 365;
}
